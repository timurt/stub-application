package kz.beesoft.wsdl;

public class Variable {
	String key;
	String path;

	public Variable() {
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}
}
